    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

    UIManager.put("OptionPane.messageFont", new Font("Sans Serif", Font.PLAIN, 14));

    try {
        UserManager userManager = new UserManager(FilePaths.USER_FILE);

        String username = tfUser.getText().trim();
        String password = new String(tfPassword.getPassword()).trim();

        // Validate input fields
        if (username.isEmpty() || password.isEmpty()) {
            new DialogManager("Login failed").showInfoMessage("Please fill in the blanks.");
            return;
        }

        // Check if username exists
        if (!userManager.isUsernameExists(username)) {
            new DialogManager("Authentication failed").showInfoMessage("Username does not exist, please try again.");
            return;
        }

        // Authenticate user
        if (userManager.authenticate(username, password)) {
            System.out.println("Successful Login by: " + username);

            // Get user role and ID
            User loggedInUser = userManager.getUserByUsername(username); // You need to create this method
            if (loggedInUser == null) {
                new DialogManager("Authentication failed").showErrorMessage("Failed to retrieve user details.");
                return;
            }

            String userID = loggedInUser.getUserID();
            String role = loggedInUser.getRole(); // Role stored as a string, e.g., "0", "1", etc.

            // Set session ID
            SessionManager.getInstance().setSessionID(userID);
            System.out.println("Session ID Set: " + userID);

            // Close login window
            dispose();
        } else {
            new DialogManager("Authentication failed").showInfoMessage("Incorrect password, please try again.");
        }

    } catch (HeadlessException e) {
        new DialogManager("An unexpected error occurred").showErrorMessage("Error, please report to the administrator.");
    }

     
        
    }//GEN-LAST:event_jButton2ActionPerformed

