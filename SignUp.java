    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        // Create an instance of ValidationManager
        ValidationManager verify = new ValidationManager();

        String username = tfUsername.getText().trim();
        String phoneNumber = tfPhoneNumber.getText().trim();
        String password = new String(tfPassword.getPassword());
        String confirmPassword = new String(tfConfirmPassword.getPassword());
        String role = choice1.getSelectedItem();
        String errorMessage = "Error(s):\n";
        boolean inputError = false;

        DialogManager dialogManager = new DialogManager("Registration failed!");

        // **Validation Using ValidationManager**
        if (!verify.nullCheck(username) || username.equals(placeholders[0])) {
            inputError = true;
            errorMessage += "- Username cannot be empty!\n";
        } else if (!verify.characterCheck(username)) {
            inputError = true;
            errorMessage += "- Username should not contain special characters!\n";
        }

        if (!verify.nullCheck(phoneNumber) || phoneNumber.equals(placeholders[3])) {
            inputError = true;
            errorMessage += "- Phone number cannot be empty!\n";
        } else if (!verify.phoneValidation(phoneNumber)) {
            inputError = true;
            errorMessage += "- Invalid phone number format!\n";
        }

        if (!verify.nullCheck(password) || password.equals(placeholders[4])) {
            inputError = true;
            errorMessage += "- Password cannot be empty!\n";
        } else if (!verify.passwordValidation(password)) {
            inputError = true;
            errorMessage += "- Password must be at least 8 characters, contain one number, and one special character!\n";
        }

        if (!verify.nullCheck(confirmPassword) || confirmPassword.equals(placeholders[5])) {
            inputError = true;
            errorMessage += "- Confirm password cannot be empty!\n";
        } else if (!confirmPassword.equals(password)) {
            inputError = true;
            errorMessage += "- Passwords do not match!\n";
        }

        if (inputError) {
            dialogManager.showErrorMessage(errorMessage);
            return;
        }

        // **Role Conversion**
        String convertedValue;
        switch (role) {
            case "Customer" -> convertedValue = "4";
            case "Vendor" -> convertedValue = "2";
            case "Runner" -> convertedValue = "3";
            case "Admin" -> convertedValue = "1";
            case "Manager" -> convertedValue = "0";
            default -> {
                dialogManager.showInfoMessage("Please select a valid role.");
                return;
            }
        }

        int roleNumber = Integer.parseInt(convertedValue);

        // **Register User**
        UserManager userManager = new UserManager(FilePaths.USER_FILE);
        boolean isRegistered = userManager.registerUser(username, phoneNumber, roleNumber, password, null);

        if (isRegistered) {
            dialogManager.setTitle("Registration successful");
            dialogManager.showInfoMessage("You have successfully registered.");
            new Login().setVisible(true);
            this.dispose();
        } else {
            dialogManager.showErrorMessage("User registration failed.");
        }
    

            
    }//GEN-LAST:event_jButton2ActionPerformed

